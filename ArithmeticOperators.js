//Operators are special symbols used to perform operations on numeric operands and produces a result
//Arithmetic Operators are used to perform mathematical operations between numeric operands.

//variables or operands

var x , y, z;

x = 15;
y = 10;

//Addition operator 

z = x + y;

document.write("The addition of 10+15 = "+z)

//Subtraction operator

z = x - y;

document.write("The subtraction of 15 - 10 = "+z);

//Multiplication Operator 

z = x * y;

document.write("The multiplication of 15*10 = "+z);

//Modulus Operator

z = x % y;

document.write("The modulus of 15 % 10 is = "+z);

